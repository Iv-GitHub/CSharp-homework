using System;

namespace DeclareVariables
{
    class DeclareVariables
    {
        static void Main()
        {
            ushort ushortVar = 52130;
            sbyte sbyteVar = -115;
            int intVar = 4825932;
            byte byteVar = 97;
            short shortVar = -10000;
            Console.WriteLine("Ushort Variable: "
                + ushortVar + "; \nSbyte Variable: "
                + sbyteVar + "; \nInt Variable: "
                + intVar + "; \nByte Variable: "
                + byteVar + "; \nShort Variable: "
                + shortVar);
        }
    }
}
