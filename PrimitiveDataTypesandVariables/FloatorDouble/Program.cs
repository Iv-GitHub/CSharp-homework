using System;

namespace FloatorDouble
{
    class FloatorDouble
    {
        static void Main()
        {
            float FloatVal = 12.345f;
            float Floatval = 3456.091f;
            double DoubleVal = 34.567839023;
            double Doubleval = 892312.1234857;
            Console.WriteLine("Float:"+ FloatVal + "; "+ Floatval
                            + "\nDouble: "+ DoubleVal + "; " + Doubleval);
        }
    }
}
